					}
					else{

					$.post("login/login_controller.php",{uname:uname,pass:pass},function(data,status){
						//alert(data);
						if(data=="1"){
							Swal.fire("Invalid username or password");
						}
						else if(data=="2"){
							Swal.fire("Account Blocked, Please Contact Administrator");
						}
						else if(data=="3"){
							//alert("Valid");
